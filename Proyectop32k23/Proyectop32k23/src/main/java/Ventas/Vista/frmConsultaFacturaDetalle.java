     public void llenadoDeTablasPedidos(int cotidSeleccionado) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id Factura");
        modelo.addColumn("Codigo Producto");
        modelo.addColumn("Precio Producto");
        modelo.addColumn("Cantidad Producto");
        modelo.addColumn("Descuento");
        modelo.addColumn("Impuestos");
        modelo.addColumn("Sub Total");
        clsFacturas cotizacion = new clsFacturas();
        List<clsFacturas> listaCotizaciones = cotizacion.getListadoCotizacionesDetalle(cotidSeleccionado);
        tblPedFacCons.setModel(modelo);
        String[] dato = new String[7];
        for (int i = 0; i < listaCotizaciones.size(); i++) {
            dato[0] = Integer.toString(listaCotizaciones.get(i).getIdFactura());
            dato[1] = Integer.toString(listaCotizaciones.get(i).getCodigoProducto());
            dato[2] = Double.toString(listaCotizaciones.get(i).getPrecioProducto());
            dato[3] = Double.toString(listaCotizaciones.get(i).getCantidadProducto());
            dato[4] = Double.toString(listaCotizaciones.get(i).getDescuentoFactura());
            dato[5] = Double.toString(listaCotizaciones.get(i).getImpuestosFactura());
            dato[6] = Double.toString(listaCotizaciones.get(i).getTotalIndividualFactura());
            
            modelo.addRow(dato);
        }              

                    

}
    


    

