     public void llenadoDeTablasCotizaciones(int cotidSeleccionado) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id Cotizacion");
        modelo.addColumn("Codigo Producto");
        modelo.addColumn("Precio");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Sub Total");
        clsCotizacion cotizacion = new clsCotizacion();
        List<clsCotizacion> listaCotizaciones = cotizacion.getListadoCotizacionesDetalle(cotidSeleccionado);
        tblCotConsDet.setModel(modelo);
        String[] dato = new String[5];
        for (int i = 0; i < listaCotizaciones.size(); i++) {
            dato[0] = Integer.toString(listaCotizaciones.get(i).getIdCot());
            dato[1] = Integer.toString(listaCotizaciones.get(i).getIdProducto());
            dato[2] = Double.toString(listaCotizaciones.get(i).getPrecioProducto());
            dato[3] = Integer.toString(listaCotizaciones.get(i).getCantidadProducto());
            dato[4] = Double.toString(listaCotizaciones.get(i).getTotalCot());
            
            modelo.addRow(dato);
        }              

                    

}
    


    

